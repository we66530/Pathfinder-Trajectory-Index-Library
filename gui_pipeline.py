def run_ransac_sparse(out_root: Path, frame_idx: int, log_widget):
    name = frame_name(frame_idx)

    left_dir, right_dir = get_rectified_dirs(out_root)

    left_path = left_dir / f"{name}.png"
    right_path = right_dir / f"{name}.png"
    csv_path = out_root / "frame_camera_imu.csv"

    ws = out_root / "per_frame" / name
    out_dir = ws / "ransac"
    out_dir.mkdir(parents=True, exist_ok=True)

    intr = read_intrinsics(csv_path, frame_idx)

    fx = intr["fx"]
    fy = intr["fy"]
    cx = intr["cx"]
    cy = intr["cy"]
    baseline = intr["baseline_m"]

    left_bgr = cv2.imread(str(left_path), cv2.IMREAD_COLOR)
    right_bgr = cv2.imread(str(right_path), cv2.IMREAD_COLOR)

    if left_bgr is None:
        raise RuntimeError(f"Failed to read left image: {left_path}")

    if right_bgr is None:
        raise RuntimeError(f"Failed to read right image: {right_path}")

    if left_bgr.shape != right_bgr.shape:
        raise RuntimeError(f"Image size mismatch: left={left_bgr.shape}, right={right_bgr.shape}")

    left_gray = cv2.cvtColor(left_bgr, cv2.COLOR_BGR2GRAY)
    right_gray = cv2.cvtColor(right_bgr, cv2.COLOR_BGR2GRAY)

    # Improve low-contrast indoor frames.
    clahe = cv2.createCLAHE(clipLimit=2.0, tileGridSize=(8, 8))
    left_gray = clahe.apply(left_gray)
    right_gray = clahe.apply(right_gray)

    orb = cv2.ORB_create(
        nfeatures=7000,
        scaleFactor=1.2,
        nlevels=8,
        edgeThreshold=15,
        patchSize=31,
        fastThreshold=10,
    )

    kpL, desL = orb.detectAndCompute(left_gray, None)
    kpR, desR = orb.detectAndCompute(right_gray, None)

    log_text(log_widget, f"[RANSAC] keypoints left={len(kpL)}, right={len(kpR)}")

    if desL is None or desR is None:
        raise RuntimeError("No ORB descriptors found. The frame may be too blurry or low-texture.")

    bf = cv2.BFMatcher(cv2.NORM_HAMMING, crossCheck=False)
    knn = bf.knnMatch(desL, desR, k=2)

    good = []
    ratio = 0.75

    for mn in knn:
        if len(mn) < 2:
            continue

        m, n = mn

        if m.distance < ratio * n.distance:
            good.append(m)

    log_text(log_widget, f"[RANSAC] matches after ratio test: {len(good)}")

    raw_vis = cv2.drawMatches(
        left_bgr,
        kpL,
        right_bgr,
        kpR,
        good[:250],
        None,
        flags=cv2.DrawMatchesFlags_NOT_DRAW_SINGLE_POINTS,
    )

    cv2.imwrite(str(out_dir / f"{name}_matches_raw.png"), raw_vis)

    filtered = []
    ptsL = []
    ptsR = []

    max_y_diff_px = 2.0
    min_disparity_px = 0.5
    max_disparity_px = 512.0

    for m in good:
        xL, yL = kpL[m.queryIdx].pt
        xR, yR = kpR[m.trainIdx].pt

        disp = xL - xR

        if abs(yL - yR) > max_y_diff_px:
            continue

        if disp <= min_disparity_px:
            continue

        if disp > max_disparity_px:
            continue

        filtered.append(m)
        ptsL.append([xL, yL])
        ptsR.append([xR, yR])

    ptsL = np.array(ptsL, dtype=np.float32)
    ptsR = np.array(ptsR, dtype=np.float32)

    log_text(log_widget, f"[RANSAC] matches after stereo filter: {len(filtered)}")

    if len(filtered) < 8:
        raise RuntimeError("Too few matches after stereo filtering.")

    F, mask = cv2.findFundamentalMat(
        ptsL,
        ptsR,
        method=cv2.FM_RANSAC,
        ransacReprojThreshold=1.0,
        confidence=0.999,
        maxIters=5000,
    )

    if mask is None:
        raise RuntimeError("RANSAC failed to produce a mask.")

    mask = mask.ravel().astype(bool)

    in_matches = [m for m, keep in zip(filtered, mask) if keep]
    ptsL_in = ptsL[mask]
    ptsR_in = ptsR[mask]

    log_text(log_widget, f"[RANSAC] inliers: {len(in_matches)}")

    if len(in_matches) < 8:
        raise RuntimeError("Too few RANSAC inliers.")

    ransac_vis = cv2.drawMatches(
        left_bgr,
        kpL,
        right_bgr,
        kpR,
        in_matches[:350],
        None,
        flags=cv2.DrawMatchesFlags_NOT_DRAW_SINGLE_POINTS,
    )

    cv2.imwrite(str(out_dir / f"{name}_matches_ransac.png"), ransac_vis)

    rows = []

    for pL, pR in zip(ptsL_in, ptsR_in):
        xL, yL = float(pL[0]), float(pL[1])
        xR, yR = float(pR[0]), float(pR[1])

        disp = xL - xR

        if disp <= 0:
            continue

        Z = fx * baseline / disp
        X = (xL - cx) * Z / fx
        Y = (yL - cy) * Z / fy

        rows.append([
            xL,
            yL,
            xR,
            yR,
            yL - yR,
            disp,
            X,
            Y,
            Z,
        ])

    arr = np.array(rows, dtype=np.float32)

    if len(arr) < 10:
        raise RuntimeError("Too few valid sparse depth points after metric conversion.")

    npy_path = out_dir / f"{name}_sparse_points.npy"
    np.save(str(npy_path), arr)

    csv_path_out = out_dir / f"{name}_sparse_points.csv"

    with open(csv_path_out, "w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow([
            "x_left",
            "y_left",
            "x_right",
            "y_right",
            "y_diff",
            "disparity_px",
            "X_m",
            "Y_m",
            "Z_m",
        ])
        writer.writerows(rows)

    overlay = left_bgr.copy()

    zvals = arr[:, 8]
    zlo = np.percentile(zvals, 5)
    zhi = np.percentile(zvals, 95)

    for p in arr:
        x = int(round(p[0]))
        y = int(round(p[1]))
        z = float(p[8])

        t = 1.0 - (z - zlo) / (zhi - zlo + 1e-6)
        t = np.clip(t, 0.0, 1.0)

        color = (
            int(255 * (1.0 - t)),
            int(255 * t),
            int(255 * t),
        )

        cv2.circle(overlay, (x, y), 4, color, -1)

    cv2.imwrite(str(out_dir / f"{name}_sparse_depth_points.png"), overlay)

    log_text(log_widget, f"[RANSAC] saved sparse points: {npy_path}")
    log_text(log_widget, f"[RANSAC] sparse point count: {len(arr)}")

    return npy_path


