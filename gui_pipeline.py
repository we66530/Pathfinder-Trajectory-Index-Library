    def do_batch_process(self):
        try:
            out_root = Path(self.out_var.get())
            encoder = self.encoder_var.get()

            all_frames = find_available_frame_indices(out_root)

            start_text = self.batch_start_var.get().strip()
            end_text = self.batch_end_var.get().strip()

            step = max(1, int(self.batch_step_var.get()))

            start_idx = int(start_text) if start_text else all_frames[0]
            end_idx = int(end_text) if end_text else all_frames[-1]

            frames = [
                idx for idx in all_frames
                if start_idx <= idx <= end_idx
            ]

            frames = frames[::step]

            if len(frames) == 0:
                raise RuntimeError("No frames selected for batch processing.")

            log_text(self.log, "\n========== BATCH PROCESS ==========")
            log_text(self.log, f"[BATCH] output root: {out_root}")
            log_text(self.log, f"[BATCH] total available frames: {len(all_frames)}")
            log_text(self.log, f"[BATCH] selected frames: {len(frames)}")
            log_text(self.log, f"[BATCH] range: {start_idx} -> {end_idx}, step={step}")
            log_text(self.log, f"[BATCH] encoder: {encoder}")
            log_text(self.log, f"[BATCH] skip existing: {self.skip_existing_var.get()}")

            success = []
            failed = []
            skipped = []

            for n, frame_idx in enumerate(frames, start=1):
                if self.stop_batch:
                    log_text(self.log, "[BATCH] Stopped by user.")
                    break

                name = frame_name(frame_idx)

                log_text(self.log, f"\n[BATCH] {n}/{len(frames)} : {name}")

                if self.skip_existing_var.get() and self.frame_output_exists(out_root, frame_idx):
                    log_text(self.log, f"[BATCH] Skip existing: {name}")
                    skipped.append(frame_idx)
                    continue

                try:
                    result = self.process_one_frame_core(
                        out_root=out_root,
                        frame_idx=frame_idx,
                        encoder=encoder,
                    )

                    success.append(result)

                except Exception as e:
                    log_text(self.log, f"[BATCH ERROR] {name} failed: {e}")

                    failed.append({
                        "frame_idx": frame_idx,
                        "frame_name": name,
                        "error": str(e),
                    })

                    # Continue batch even if a single frame fails.
                    continue

            summary = {
                "output_root": str(out_root),
                "encoder": encoder,
                "start_idx": start_idx,
                "end_idx": end_idx,
                "step": step,
                "success_count": len(success),
                "failed_count": len(failed),
                "skipped_count": len(skipped),
                "success": success,
                "failed": failed,
                "skipped": skipped,
            }

            summary_path = out_root / "per_frame" / "batch_summary.json"
            summary_path.parent.mkdir(parents=True, exist_ok=True)

            with open(summary_path, "w", encoding="utf-8") as f:
                json.dump(summary, f, indent=2, ensure_ascii=False)

            log_text(self.log, "\n========== BATCH SUMMARY ==========")
            log_text(self.log, f"[BATCH] success: {len(success)}")
            log_text(self.log, f"[BATCH] failed : {len(failed)}")
            log_text(self.log, f"[BATCH] skipped: {len(skipped)}")
            log_text(self.log, f"[BATCH] summary saved: {summary_path}")

            if len(failed) > 0:
                log_text(self.log, "\n[BATCH] Failed frames:")

                for item in failed:
                    log_text(
                        self.log,
                        f"       {item['frame_name']}: {item['error']}"
                    )

            log_text(self.log, "\n[DONE] Batch processing finished.")

        except Exception as e:
            log_text(self.log, f"[ERROR] {e}")
            messagebox.showerror("Error", str(e))

    def do_full(self):
        self.do_export()
        self.do_process_frame()


