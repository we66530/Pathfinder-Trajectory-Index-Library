    shutil.copy2(right_src, right_dst)

    txt_path = ws / "pair.txt"
    txt_path.write_text(
        str(left_dst) + "\n" + str(right_dst) + "\n",
        encoding="utf-8",
    )

    return ws, left_src, right_src, left_dst, right_dst, txt_path


