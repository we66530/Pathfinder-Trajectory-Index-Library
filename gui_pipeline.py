def frame_name(frame_idx: int) -> str:
    return f"frame_{frame_idx:06d}"


def log_text(widget, msg: str):
    widget.insert(tk.END, msg + "\n")
    widget.see(tk.END)
    widget.update_idletasks()


def run_cmd(cmd, log_widget=None, cwd=None):
    if log_widget:
        cmd_str = " ".join(f'"{c}"' if " " in str(c) else str(c) for c in cmd)
        log_text(log_widget, "[CMD] " + cmd_str)

    p = subprocess.Popen(
        cmd,
        cwd=cwd,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        encoding="utf-8",
        errors="replace",
    )

    for line in p.stdout:
        if log_widget:
            log_text(log_widget, line.rstrip())

    code = p.wait()

    if code != 0:
        raise RuntimeError(f"Command failed with exit code {code}")


def read_intrinsics(csv_path: Path, frame_idx: int):
    if not csv_path.exists():
        raise FileNotFoundError(f"CSV not found: {csv_path}")

    with open(csv_path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)

        for row in reader:
            if int(row["frame_index"]) == frame_idx:
                return {
                    "fx": float(row["left_fx"]),
                    "fy": float(row["left_fy"]),
                    "cx": float(row["left_cx"]),
                    "cy": float(row["left_cy"]),
                    "baseline_m": float(row["baseline_m"]),
                }

    raise RuntimeError(f"frame_index={frame_idx} not found in {csv_path}")


