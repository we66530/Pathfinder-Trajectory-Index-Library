def run_pointcloud(out_root: Path, frame_idx: int, dense_npy: Path, log_widget):
    name = frame_name(frame_idx)

    left_dir, _ = get_rectified_dirs(out_root)

    left_path = left_dir / f"{name}.png"
    csv_path = out_root / "frame_camera_imu.csv"

    ws = out_root / "per_frame" / name
    out_dir = ws / "pointcloud"
    out_dir.mkdir(parents=True, exist_ok=True)

    intr = read_intrinsics(csv_path, frame_idx)

    fx = intr["fx"]
    fy = intr["fy"]
    cx = intr["cx"]
    cy = intr["cy"]

    depth = np.load(str(dense_npy)).astype(np.float32)
    left = cv2.imread(str(left_path), cv2.IMREAD_COLOR)

    if left is None:
        raise RuntimeError(f"Failed to read left image: {left_path}")

    H, W = depth.shape
    h0, w0 = left.shape[:2]

    # If Depth Anything output size differs from image size, resize image and scale intrinsics.
    if (W != w0) or (H != h0):
        sx = W / w0
        sy = H / h0

        left = cv2.resize(left, (W, H), interpolation=cv2.INTER_LINEAR)

        fx *= sx
        fy *= sy
        cx *= sx
        cy *= sy

        log_text(log_widget, "[PLY] resized left image and scaled intrinsics to match dense depth size.")

    stride = 1

    ys = np.arange(0, H, stride, dtype=np.float32)
    xs = np.arange(0, W, stride, dtype=np.float32)

    uu, vv = np.meshgrid(xs, ys)

    u = uu.reshape(-1)
    v = vv.reshape(-1)

    z = depth[v.astype(np.int32), u.astype(np.int32)]

    valid = (
        np.isfinite(z) &
        (z >= 0.2) &
        (z <= 20.0)
    )

    u = u[valid]
    v = v[valid]
    z = z[valid]

    X = (u - cx) * z / fx
    Y = (v - cy) * z / fy
    Z = z

    xyz = np.stack([X, Y, Z], axis=1).astype(np.float32)

    bgr = left[v.astype(np.int32), u.astype(np.int32)]
    rgb = bgr[:, ::-1].astype(np.uint8)

    ply_path = out_dir / f"{name}_left_depth_pointcloud.ply"
    write_binary_ply_xyzrgb(ply_path, xyz, rgb)

    xyzrgb = np.concatenate([xyz, rgb.astype(np.float32)], axis=1)
    xyzrgb_path = out_dir / f"{name}_left_depth_xyzrgb.npy"
    np.save(str(xyzrgb_path), xyzrgb)

    report_path = out_dir / f"{name}_pointcloud_report.json"

    report = {
        "frame_index": frame_idx,
        "dense_depth_npy": str(dense_npy),
        "left_image": str(left_path),
        "ply": str(ply_path),
        "xyzrgb_npy": str(xyzrgb_path),
        "point_count": int(len(xyz)),
        "coordinate_convention": {
            "X": "right",
            "Y": "down",
            "Z": "forward",
        },
        "depth_range_filter_m": [0.2, 20.0],
    }

    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)

    log_text(log_widget, f"[PLY] saved: {ply_path}")
    log_text(log_widget, f"[PLY] points: {len(xyz)}")

    return ply_path


