def nearest_sample(img, xs, ys):
    h, w = img.shape[:2]

    xi = np.round(xs).astype(np.int32)
    yi = np.round(ys).astype(np.int32)

    valid = (
        (xi >= 0) &
        (xi < w) &
        (yi >= 0) &
        (yi < h)
    )

    out = np.full(xs.shape, np.nan, dtype=np.float32)
    out[valid] = img[yi[valid], xi[valid]]

    return out, valid


def fit_line(x, y):
    A = np.stack([x, np.ones_like(x)], axis=1)
    a, b = np.linalg.lstsq(
        A.astype(np.float64),
        y.astype(np.float64),
        rcond=None,
    )[0]

    return float(a), float(b)


def predict_from_model(rel, model):
    mode = model["mode"]
    a = model["a"]
    b = model["b"]

    rel = rel.astype(np.float32)

    if mode == "linear_z":
        return (a * rel + b).astype(np.float32)

    if mode == "inverse_z":
        denom = a * rel + b
        out = np.full_like(rel, np.nan, dtype=np.float32)
        valid = denom > 1e-8
        out[valid] = 1.0 / denom[valid]
        return out

    raise ValueError(f"Unknown model mode: {mode}")


def ransac_fit_model(d, z, mode="inverse_z", iters=3000, thr=0.75):
    rng = np.random.default_rng(42)

    n = len(d)

    if n < 2:
        raise RuntimeError("Need at least two anchors for fitting.")

    if mode == "linear_z":
        x = d
        y = z
    elif mode == "inverse_z":
        x = d
        y = 1.0 / np.maximum(z, 1e-6)
    else:
        raise ValueError(mode)

    best = None

    for _ in range(iters):
        ids = rng.choice(n, 2, replace=False)

        if abs(float(x[ids[0]] - x[ids[1]])) < 1e-8:
            continue

        a, b = fit_line(x[ids], y[ids])

        model = {
            "mode": mode,
            "a": a,
            "b": b,
        }

        pred = predict_from_model(d, model)

        residual = np.abs(pred - z)
        inliers = np.isfinite(pred) & (pred > 0) & (residual < thr)

        count = int(np.count_nonzero(inliers))

        if count == 0:
            continue

        med = float(np.median(residual[inliers]))

        score = (count, -med)

        if best is None or score > best["score"]:
            best = {
                "mode": mode,
                "a": a,
                "b": b,
                "inliers": inliers,
                "count": count,
                "median_inlier_residual": med,
                "score": score,
            }

    if best is None:
        a, b = fit_line(x, y)
        best = {
            "mode": mode,
            "a": a,
            "b": b,
            "inliers": np.ones(n, dtype=bool),
            "count": n,
            "median_inlier_residual": 999.0,
            "score": (n, -999.0),
        }

    # Refit using inliers.
    inl = best["inliers"]

    if np.count_nonzero(inl) >= 2:
        a, b = fit_line(x[inl], y[inl])
        best["a"] = a
        best["b"] = b

        pred = predict_from_model(d, best)
        residual = np.abs(pred - z)
        ok = np.isfinite(pred) & (pred > 0)
        inliers2 = ok & (residual < thr)

        best["inliers"] = inliers2
        best["count"] = int(np.count_nonzero(inliers2))
        best["median_inlier_residual"] = (
            float(np.median(residual[inliers2]))
            if np.count_nonzero(inliers2)
            else 999.0
        )

    return best


def make_anchor_confidence_map(shape, xs, ys, sigma_px=120.0):
    h, w = shape

    mask = np.ones((h, w), dtype=np.uint8) * 255

    for x, y in zip(xs, ys):
        xi = int(round(x))
        yi = int(round(y))

        if 0 <= xi < w and 0 <= yi < h:
            cv2.circle(mask, (xi, yi), 3, 0, -1)

    dist = cv2.distanceTransform(mask, cv2.DIST_L2, 5)
    conf = np.exp(-dist / max(sigma_px, 1e-6)).astype(np.float32)

    return conf

def robust_normalize_0to1(x, p_low=2, p_high=98):
    x = x.astype(np.float32)
    valid = np.isfinite(x)

    out = np.zeros_like(x, dtype=np.float32)

    if np.count_nonzero(valid) == 0:
        return out

    lo = np.percentile(x[valid], p_low)
    hi = np.percentile(x[valid], p_high)

    out = (x - lo) / (hi - lo + 1e-8)
    out = np.clip(out, 0.0, 1.0)

    return out


def safe_corrcoef(a, b):
    a = np.asarray(a, dtype=np.float32)
    b = np.asarray(b, dtype=np.float32)

    valid = np.isfinite(a) & np.isfinite(b)

    if np.count_nonzero(valid) < 8:
        return 0.0

    a = a[valid]
    b = b[valid]

    if np.std(a) < 1e-8 or np.std(b) < 1e-8:
        return 0.0

    return float(np.corrcoef(a, b)[0, 1])


def make_shape_preserving_unit_depth(
    rel_depth,
    high_relative_is_near=True,
    far_to_near_ratio=12.0,
):
    """
    Convert Depth Anything relative depth into unit metric-like depth.

    Output is not yet in meters.
    It preserves the relative depth map shape.

    far_to_near_ratio:
        if near unit depth = 1,
        far unit depth = far_to_near_ratio.

    We use inverse-depth interpolation:

        invZ = invZ_far + r * (invZ_near - invZ_far)

    where r=1 means near, r=0 means far.
    """
    r0 = robust_normalize_0to1(rel_depth, p_low=2, p_high=98)

    if high_relative_is_near:
        r = r0
    else:
        r = 1.0 - r0

    near_unit_z = 1.0
    far_unit_z = float(far_to_near_ratio)

    inv_near = 1.0 / near_unit_z
    inv_far = 1.0 / far_unit_z

    inv_z = inv_far + r * (inv_near - inv_far)

    unit_z = 1.0 / np.maximum(inv_z, 1e-8)
    unit_z = unit_z.astype(np.float32)

    return unit_z, r


def robust_scale_fit(unit_z_anchor, z_anchor_m, abs_thr_m=1.5, rel_thr=0.40, n_iter=5):
    """
    Fit only one scalar:

        Z_metric = scale * Z_unit

    This preserves the full shape of Depth Anything depth.
    RANSAC sparse points only calibrate scale.
    """
    unit_z_anchor = unit_z_anchor.astype(np.float32)
    z_anchor_m = z_anchor_m.astype(np.float32)

    valid = (
        np.isfinite(unit_z_anchor) &
        np.isfinite(z_anchor_m) &
        (unit_z_anchor > 1e-6) &
        (z_anchor_m > 0)
    )

    unit_z_anchor = unit_z_anchor[valid]
    z_anchor_m = z_anchor_m[valid]

    if len(unit_z_anchor) < 5:
        raise RuntimeError("Too few valid anchors for scale fitting.")

    ratios = z_anchor_m / unit_z_anchor

    scale = float(np.median(ratios))
    inliers = np.ones(len(ratios), dtype=bool)

    for _ in range(n_iter):
        pred = scale * unit_z_anchor
        residual = np.abs(pred - z_anchor_m)

        thr = np.maximum(abs_thr_m, rel_thr * z_anchor_m)
        inliers = residual <= thr

        if np.count_nonzero(inliers) < 5:
            break

        scale = float(np.median(ratios[inliers]))

    pred = scale * unit_z_anchor
    residual = np.abs(pred - z_anchor_m)

    return {
        "scale": scale,
        "inliers": inliers,
        "inlier_count": int(np.count_nonzero(inliers)),
        "median_residual_m": float(np.median(residual[inliers])) if np.count_nonzero(inliers) else float(np.median(residual)),
        "p90_residual_m": float(np.percentile(residual[inliers], 90)) if np.count_nonzero(inliers) else float(np.percentile(residual, 90)),
        "all_residual": residual,
    }


def save_metric_depth_vis(depth_m, out_path, mode="auto", min_depth=0.2, max_depth=10.0):
    """
    Save depth visualization.
    Near = warm/red, far = blue.

    mode='auto':
        use p2-p98 range, better for checking shape.

    mode='fixed':
        use fixed min_depth-max_depth, better for metric inspection.
    """
    depth_m = depth_m.astype(np.float32)
    valid = np.isfinite(depth_m) & (depth_m > 0)

    if np.count_nonzero(valid) == 0:
        vis_u8 = np.zeros(depth_m.shape, dtype=np.uint8)
        color = cv2.applyColorMap(vis_u8, cv2.COLORMAP_TURBO)
        cv2.imwrite(str(out_path), color)
        return

    if mode == "auto":
        lo = float(np.percentile(depth_m[valid], 2))
        hi = float(np.percentile(depth_m[valid], 98))
    else:
        lo = float(min_depth)
        hi = float(max_depth)

    d = np.clip(depth_m, lo, hi)

    # near should be high value in visualization
    vis = 1.0 - (d - lo) / (hi - lo + 1e-8)
    vis = np.clip(vis, 0.0, 1.0)

    vis_u8 = (vis * 255).astype(np.uint8)
    color = cv2.applyColorMap(vis_u8, cv2.COLORMAP_TURBO)

    color[~valid] = (0, 0, 0)

    cv2.imwrite(str(out_path), color)


def run_fit_dense(out_root: Path, frame_idx: int, sparse_npy: Path, left_rel_npy: Path, log_widget):
    """
    Shape-preserving dense metric depth fitting.

    Key idea:
    - Depth Anything relative depth controls dense shape.
    - RANSAC sparse stereo points only calibrate the metric scale.

    This prevents the dense map from collapsing into a flat plane when
    RANSAC points are concentrated on trees / high-texture regions.
    """
    name = frame_name(frame_idx)

    ws = out_root / "per_frame" / name
    out_dir = ws / "dense_metric_depth"
    out_dir.mkdir(parents=True, exist_ok=True)

    csv_path = out_root / "frame_camera_imu.csv"
    intr = read_intrinsics(csv_path, frame_idx)

    fx = intr["fx"]
    baseline = intr["baseline_m"]

    sparse = np.load(str(sparse_npy)).astype(np.float32)
    rel = np.load(str(left_rel_npy)).astype(np.float32)

    if sparse.ndim != 2 or sparse.shape[1] < 9:
        raise RuntimeError(f"Unexpected sparse points shape: {sparse.shape}")

    if rel.ndim != 2:
        raise RuntimeError(f"Expected 2D relative depth map, got shape={rel.shape}")

    xL = sparse[:, 0]
    yL = sparse[:, 1]
    disp = sparse[:, 5]

    # Recompute sparse metric depth from disparity and intrinsics.
    z_sparse_m = fx * baseline / np.maximum(disp, 1e-6)

    # Sample relative depth at sparse point positions.
    d_anchor_raw, inside = nearest_sample(rel, xL, yL)

    valid = (
        inside &
        np.isfinite(d_anchor_raw) &
        np.isfinite(z_sparse_m) &
        (z_sparse_m > 0.2) &
        (z_sparse_m < 30.0) &
        (disp > 0.5)
    )

    d_anchor_raw = d_anchor_raw[valid].astype(np.float32)
    z_anchor_m = z_sparse_m[valid].astype(np.float32)
    x_anchor = xL[valid].astype(np.float32)
    y_anchor = yL[valid].astype(np.float32)

    log_text(log_widget, f"[FIT] valid sparse anchors: {len(d_anchor_raw)}")

    if len(d_anchor_raw) < 10:
        raise RuntimeError("Too few valid anchors for dense metric fitting.")

    # ------------------------------------------------------------
    # 1. Decide orientation:
    #    Depth Anything usually gives larger value for closer regions.
    #    But we still check correlation with inverse metric depth.
    # ------------------------------------------------------------
    rel_norm = robust_normalize_0to1(rel, p_low=2, p_high=98)
    d_anchor_norm, _ = nearest_sample(rel_norm, x_anchor, y_anchor)

    inv_z_anchor = 1.0 / np.maximum(z_anchor_m, 1e-6)
    corr = safe_corrcoef(d_anchor_norm, inv_z_anchor)

    # Conservative rule:
    # Only flip if there is a clearly negative correlation.
    high_relative_is_near = True

    if corr < -0.25:
        high_relative_is_near = False

    log_text(log_widget, f"[FIT] corr(relative_depth, inverse_Z) = {corr:.4f}")
    log_text(log_widget, f"[FIT] high_relative_is_near = {high_relative_is_near}")

    # ------------------------------------------------------------
    # 2. Build unit depth from relative depth while preserving shape.
    # ------------------------------------------------------------
    # Tune this if point cloud depth range is too flat or too exaggerated.
    #
    # Larger value = stronger near/far separation.
    # Try 8, 12, 20, 30.
    FAR_TO_NEAR_RATIO = 12.0

    unit_depth, rel_near_score = make_shape_preserving_unit_depth(
        rel,
        high_relative_is_near=high_relative_is_near,
        far_to_near_ratio=FAR_TO_NEAR_RATIO,
    )

    unit_anchor, inside_unit = nearest_sample(unit_depth, x_anchor, y_anchor)

    valid2 = (
        inside_unit &
        np.isfinite(unit_anchor) &
        np.isfinite(z_anchor_m) &
        (unit_anchor > 0) &
        (z_anchor_m > 0)
    )

    unit_anchor = unit_anchor[valid2]
    z_anchor_m2 = z_anchor_m[valid2]
    x_anchor2 = x_anchor[valid2]
    y_anchor2 = y_anchor[valid2]

    # Diagnostic: how much of relative depth range is covered by sparse anchors?
    anchor_rel_norm, _ = nearest_sample(rel_near_score, x_anchor2, y_anchor2)

    anchor_rel_p05 = float(np.percentile(anchor_rel_norm, 5))
    anchor_rel_p95 = float(np.percentile(anchor_rel_norm, 95))
    anchor_rel_coverage = anchor_rel_p95 - anchor_rel_p05

    log_text(log_widget, f"[FIT] anchor relative-depth coverage p05-p95 = {anchor_rel_coverage:.3f}")

    if anchor_rel_coverage < 0.20:
        log_text(
            log_widget,
            "[FIT WARN] Sparse anchors cover a narrow relative-depth range. "
            "Metric scale may be biased, but dense shape will still follow Depth Anything."
        )

    # ------------------------------------------------------------
    # 3. Fit only metric scale.
    # ------------------------------------------------------------
    scale_result = robust_scale_fit(
        unit_z_anchor=unit_anchor,
        z_anchor_m=z_anchor_m2,
        abs_thr_m=1.5,
        rel_thr=0.40,
        n_iter=5,
    )

    scale = scale_result["scale"]
    inlier_mask = scale_result["inliers"]

    log_text(log_widget, f"[FIT] shape-preserving scale = {scale:.6f}")
    log_text(log_widget, f"[FIT] scale inliers = {scale_result['inlier_count']}/{len(unit_anchor)}")
    log_text(log_widget, f"[FIT] median residual = {scale_result['median_residual_m']:.3f} m")
    log_text(log_widget, f"[FIT] p90 residual = {scale_result['p90_residual_m']:.3f} m")

    # ------------------------------------------------------------
    # 4. Generate dense metric depth.
    # ------------------------------------------------------------
    dense = scale * unit_depth
    dense = dense.astype(np.float32)

    invalid = ~np.isfinite(dense) | (dense <= 0)

    if np.count_nonzero(~invalid) == 0:
        raise RuntimeError("Dense depth prediction is entirely invalid.")

    median_valid = float(np.nanmedian(dense[~invalid]))
    dense[invalid] = median_valid

    # Allow deeper outdoor scene.
    dense = np.clip(dense, 0.2, 30.0).astype(np.float32)

    dense_path = out_dir / f"{name}_dense_metric_depth_m.npy"
    np.save(str(dense_path), dense)

    # ------------------------------------------------------------
    # 5. Save visualizations.
    # ------------------------------------------------------------
    # Main visualization now uses auto-range, so it should visually preserve the relative depth structure.
    save_metric_depth_vis(
        dense,
        out_dir / f"{name}_dense_metric_depth_vis.png",
        mode="auto",
    )

    # Also save fixed 0.2-10 m visualization for metric inspection.
    save_metric_depth_vis(
        dense,
        out_dir / f"{name}_dense_metric_depth_vis_fixed_0p2to10m.png",
        mode="fixed",
        min_depth=0.2,
        max_depth=10.0,
    )

    # Relative depth visualization for comparison.
    rel_vis = robust_normalize_0to1(rel, p_low=2, p_high=98)
    rel_color = cv2.applyColorMap((rel_vis * 255).astype(np.uint8), cv2.COLORMAP_TURBO)
    cv2.imwrite(str(out_dir / f"{name}_relative_depth_vis.png"), rel_color)

    # ------------------------------------------------------------
    # 6. Confidence map from inlier anchor proximity.
    # ------------------------------------------------------------
    if len(inlier_mask) == len(x_anchor2):
        conf_x = x_anchor2[inlier_mask]
        conf_y = y_anchor2[inlier_mask]
    else:
        conf_x = x_anchor2
        conf_y = y_anchor2

    conf = make_anchor_confidence_map(
        dense.shape,
        conf_x,
        conf_y,
        sigma_px=120.0,
    )

    conf_path = out_dir / f"{name}_confidence_map.npy"
    np.save(str(conf_path), conf.astype(np.float32))

    conf_u8 = (np.clip(conf, 0.0, 1.0) * 255).astype(np.uint8)
    conf_color = cv2.applyColorMap(conf_u8, cv2.COLORMAP_TURBO)
    cv2.imwrite(str(out_dir / f"{name}_confidence_map_vis.png"), conf_color)

    # ------------------------------------------------------------
    # 7. Save fitting report.
    # ------------------------------------------------------------
    report = {
        "frame_index": frame_idx,
        "method": "shape_preserving_inverse_depth_scale",
        "description": (
            "Depth Anything relative depth controls dense depth shape. "
            "RANSAC sparse stereo points calibrate only the metric scale."
        ),
        "high_relative_is_near": bool(high_relative_is_near),
        "corr_relative_vs_inverse_z": float(corr),
        "far_to_near_ratio_unit_depth": float(FAR_TO_NEAR_RATIO),
        "scale": float(scale),
        "scale_inlier_count": int(scale_result["inlier_count"]),
        "scale_anchor_count": int(len(unit_anchor)),
        "median_residual_m": float(scale_result["median_residual_m"]),
        "p90_residual_m": float(scale_result["p90_residual_m"]),
        "anchor_relative_depth_coverage_p05_p95": float(anchor_rel_coverage),
        "dense_depth_stats_m": {
            "min": float(np.min(dense)),
            "p05": float(np.percentile(dense, 5)),
            "median": float(np.median(dense)),
            "p95": float(np.percentile(dense, 95)),
            "max": float(np.max(dense)),
        },
        "outputs": {
            "dense_depth_npy": str(dense_path),
            "dense_depth_vis_auto": str(out_dir / f"{name}_dense_metric_depth_vis.png"),
            "dense_depth_vis_fixed_0p2to10m": str(out_dir / f"{name}_dense_metric_depth_vis_fixed_0p2to10m.png"),
            "relative_depth_vis": str(out_dir / f"{name}_relative_depth_vis.png"),
            "confidence_map_npy": str(conf_path),
        },
        "notes": [
            "If the point cloud is still too flat, increase FAR_TO_NEAR_RATIO, e.g. 20 or 30.",
            "If the point cloud is too stretched, decrease FAR_TO_NEAR_RATIO, e.g. 8.",
            "If sparse anchors are concentrated on trees, metric scale can still be biased, but the dense shape will no longer collapse to a plane.",
        ],
    }

    report_path = out_dir / f"{name}_fit_report.json"

    with open(report_path, "w", encoding="utf-8") as f:
        json.dump(report, f, indent=2, ensure_ascii=False)

    log_text(log_widget, f"[FIT] dense depth saved: {dense_path}")
    log_text(log_widget, f"[FIT] auto-range vis saved: {out_dir / f'{name}_dense_metric_depth_vis.png'}")
    log_text(log_widget, f"[FIT] fixed-range vis saved: {out_dir / f'{name}_dense_metric_depth_vis_fixed_0p2to10m.png'}")

    return dense_path


